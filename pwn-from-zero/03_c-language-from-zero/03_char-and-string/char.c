#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>

int main(void) {

	// 문자 상수 구현 방법

	int ch;
	
	// 'a'는 ASCII 코드 97 → 문자 ↔ 정수 연동 이해 필요
	printf("문자 a의 아스키 코드 값: %d\n", 'a');

	// 숫자 97은 문자 'a' → 메모리 상 문자 분석 감각 훈련
	printf("아스키 코드값이 97인 문자: %c\n", 97);

	// 문자 상수는 int 타입으로 처리됨 → sizeof('a') == 4
	printf("문자 상수의 크기: %d바이트\n", sizeof('a'));

	// 문자 연산 가능 → 'a' + 1 = 'b' 감각은 문자열 분석에 유용
	ch = 'a';
	ch++;
	printf("문자 %c의 아스키 코드 값: %d\n", ch, ch);

	printf("\n\n"); //--------------------------------------------------------------------------------

	// 아스키 코드

	char small, cap = 'G'; // 아스키 코드 71

	if ((cap >= 'A') && (cap <= 'Z')) {
		// 대문자 → 소문자: ASCII 간격만큼 더함 ('G' → 'g')
		small = cap + ('a' - 'A'); // 71 ('G') + (97 - 65) → 71 + 32 → 103 ('g')
	}

	printf("대문자: %c %c", cap, '\n');
	printf("소문자: %c", small);

	printf("\n\n"); //--------------------------------------------------------------------------------

	// scanf 함수를 사용한 문자 입력

	char ch1, ch2;

	// scanf("%c %c", &ch1, &ch2);	// %c 변환문자는 공백문자, 탭문자, 개행문자도 입력함.
	// printf("[%c %c]\n", ch1, ch2);
	// printf("[%d %d]", ch1, ch2); // 아스키 코드로 출력

	printf("\n\n"); //--------------------------------------------------------------------------------

	// getchar 함수와 putchar 함수

	int ch3;

	// getchar: 표준 입력(stdin)에서 문자 1개를 입력받아 정수형으로 반환 (아스키 코드값)
	// 시스템 해킹에서는 read(0, buf, 1) 구조와 유사 → 입력 흐름 추적 시 사용
	ch3 = getchar();

	printf("입력한 문자: ");

	// putchar: 문자 1개 출력 (아스키 코드값 기반 출력)
	// 시스템 해킹에서는 write(1, &ch3, 1)와 동일 개념 → 출력 루틴 분석 시 사용
	putchar(ch3);
	putchar('\n');

}